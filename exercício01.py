"""

EXERCÍCIO:
01. - Calcular a média de três números dados pelo usuário

"""

# - Obter o 1º número (ed)
n1 = float(input("Digite o 1º número:"))

# - Obter o 2º número (ed)
n2 = float(input("Digite o 2º número:"))

# - Obter o 3º número (ed)
n3 = float(input("Digite o 3º número:"))

# - Calcular a média (pd)
med = (n1 + n2 + n3) / 2

# - Exibir a média (sd)
print("Média =", n1, "com", n2, "e", n3, "é", med)