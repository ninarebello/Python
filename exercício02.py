# - Obter o primeiro número (ed)
n1 = float(input("Digite o 1º número:"))

# - Obter o segundo número (ed)
n2 = float(input("Digite o 2º número:"))

# - Calcular a soma (pd)
som = n1 + n2

# - Exibir a soma (sd)
print("Soma =", som)

# - Calcular a subtração (pd)
sub = n1 - n2

# - Exibir a soma (sd)
print("Subtração = ", sub)

# - Calcular a multiplicação (pd)
mult = n1 * n2

# - Exibir a multiplicação (sd)
print("Multiplicação = ", mult)

# - Calcular a divisão (pd)
div = n1 / n2

# - Exibir a divisão (sd)
print("Dvisão =", div)
